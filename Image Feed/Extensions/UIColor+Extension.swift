import UIKit

extension UIColor {
    static var ypBackground: UIColor {
        return UIColor(named: "YP Background") ?? .black
    }

    static var ypBlack: UIColor {
        return UIColor(named: "YP Black") ?? .black
    }

    static var ypGray: UIColor {
        return UIColor(named: "YP Gray") ?? .black
    }
    
    static var ypGreen: UIColor {
        return UIColor(named: "YP Green") ?? .black
    }

    static var ypRed: UIColor {
        return UIColor(named: "YP Red") ?? .black
    }

    static var ypWhite: UIColor {
        return UIColor(named: "YP White") ?? .black
    }
}
